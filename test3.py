# Arithmetic test
x = 8
y = 4
print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x % y =", x % y)

# Boolean test
a = True
b = False
print("a and b =", a and b)
print("a or b =", a or b)
print("not a =", not a)

# Bitwise test
m = 0b1010  # 10
n = 0b1100  # 12
print("m & n =", bin(m & n))
print("m | n =", bin(m | n))
print("m ^ n =", bin(m ^ n))
print("~m =", bin(~m))
